    private void btCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastroActionPerformed
        if (txtUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Digite um nome de USU√ÅRIO.");
        } else {
            if (txtEmail.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Digite um E-mail v√°lido.");
            } else {
                if (txtTelefone.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(rootPane, "Digite um TELEFONE v√°lido.");
                } else {
                    if (new String(pwSenha.getPassword()).isEmpty()) {
                        JOptionPane.showMessageDialog(rootPane, "Digite uma SENHA.");
                    }
                }
            }
        }

        try {
            confirmaUsuario(txtEmail.getText());
        } catch (Exception ex) {
            Logger.getLogger(TelaNovaConta.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (txtEmail.getText().equals(null)) {
            JOptionPane.showMessageDialog(rootPane, "Digite outro E-mail");
        } else {
            if (txtEmail.getText().equals("E-mail")) {
                JOptionPane.showMessageDialog(rootPane, "Digite outro E-mail");
            } else {
                if (txtEmail.getText().equals(email)) {
                    JOptionPane.showMessageDialog(rootPane, "Digite outro E-mail");
                } else {
                    try {
                        addUsuario(String.valueOf(pwSenha.getPassword()), txtEmail.getText(), TipoUsu);
                    } catch (Exception ex) {
                        Logger.getLogger(TelaNovaConta.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    try {
                        idUsuario(txtEmail.getText());
                    } catch (Exception ex) {
                        Logger.getLogger(TelaNovaConta.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    try {
                        addCliente(txtTelefone.getText(), txtUsuario.getText(), idUsu);
                    } catch (Exception ex) {
                        Logger.getLogger(TelaNovaConta.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    this.dispose();
                }
            }
        }


    }//GEN-LAST:event_btCadastroActionPerformed

    public void confirmaUsuario(String Conta) throws Exception {
        Conexao con = new Conexao();
        con.getConexao();

        String txtSQL = "";

        txtSQL = "select Email from tb_Usuario where Email = '" + Conta + "';";

        Statement banco = (Statement) Conexao.conexao.createStatement();
        ResultSet result = banco.executeQuery(txtSQL);

        while (result.next()) {
            email = result.getString("Email");
        }
    }

    public void addUsuario(String Senha,
            String Email,
            int Tipos
    ) throws Exception {

        Conexao con = new Conexao();
        con.getConexao();

        String txtSQL = "";

        txtSQL = "insert into tb_Usuario (Senha , Email , Data_Cadastro , idTipo_Usu) "
                + " Values ('" + Senha + "','" + Email + "', current_timestamp() ,'" + Tipos + "');";
        Statement banco = (Statement) Conexao.conexao.createStatement();
        int result = banco.executeUpdate(txtSQL);

    }

    public void addCliente(String Tel,
            String Nome,
            int Tipos) throws Exception {

        Conexao con = new Conexao();
        con.getConexao();

        String txtSQL = "";

        txtSQL = " Update tb_Cliente "
                + "set Telefone = '" + Tel + "', "
                + "Nome = '" + Nome + "' "
                + "Where 1 = 1 "
                + "and  idUsuario = '" + Tipos + "';";

        Statement banco = (Statement) Conexao.conexao.createStatement();
        int result = banco.executeUpdate(txtSQL);
    }

    public void idUsuario(String Email) throws Exception {

        Conexao con = new Conexao();
        con.getConexao();

        String txtSQL = "";

        txtSQL = "select max(idUsuario) as idUsuario from tb_Usuario where Email = '" + Email + "';";

        Statement banco = (Statement) Conexao.conexao.createStatement();
        ResultSet result = banco.executeQuery(txtSQL);

        while (result.next()) {
            idUsu = result.getInt("idUsuario");
        }
    }


